    public void readTable(){
        
        DefaultTableModel modelo = (DefaultTableModel)jTableFuncionarios.getModel();
        modelo.setNumRows(0);
        FuncionarioDAO fdao = new FuncionarioDAO();
        
        for (Funcionario func: fdao.listar()){
            
            modelo.addRow(new Object[]{
                func.getNome(),
                func.getCpf(),
                func.getDataInicio(),
                func.getUsuario(),
                func.getFuncao()
            });
            
        }
    }
    
    
