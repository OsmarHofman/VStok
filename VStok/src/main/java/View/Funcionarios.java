    private void btnFuncSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFuncSalvarActionPerformed
        verificaCampos();
        if (verificaCampos() == true){
        btnFuncSalvar.setVisible(true);
        func.setNome(txtFuncNome.getText());
        func.setCpf(jFormattedTextFieldFuncCPF.getText());
        func.setAtivo(true);
        func.setDataInicio(jFormattedTextFieldFuncDataInicio.getText());
        func.setUsuario(txtFuncUsuario.getText());
        func.setSenha(jPasswordFieldFuncSenha.getText());
        func.setFuncao((String) jComboBoxCargo.getSelectedItem());
        user.setLogin(txtFuncUsuario.getText());
        user.setSenha(jPasswordFieldFuncSenha.getText());
        user.setFuncao((String) jComboBoxCargo.getSelectedItem());
        userDao.save(user);
        dao.save(func);
        txtFuncNome.setEnabled(false);
        jFormattedTextFieldFuncDataInicio.setEnabled(false);
        txtFuncUsuario.setEnabled(false);
        jPasswordFieldFuncSenha.setEnabled(false);
        btnFuncSalvar.setEnabled(false);
        jRadioButtonFuncAtivo.setEnabled(false);
        txtFuncNome.setText("");
        txtFuncUsuario.setText("");
        jFormattedTextFieldFuncDataInicio.setText("");
        jPasswordFieldFuncSenha.setText("");
        btnFuncEditar.setEnabled(false);
        btnFuncExcluir.setEnabled(false);
        readTable();}
        else{
            JOptionPane.showMessageDialog(null, "Por favor preencha todos os campos");
        }
    }//GEN-LAST:event_btnFuncSalvarActionPerformed

